#include <iostream>
using namespace std;

int main () {
    int i ;
    i = 1 ;
    cout << ++i * i++ ;
    /*
     * Программа принимает значение i равное 1.
     * После чего выводя значение, а она умножает
     * префиксный инкремент ( увеличивает значение i
     * на 1) и  постфиксный инкрмент ( оставлеяет
     * значение i и после выполнения действия увеличивает
     * его на 1)
     */

}